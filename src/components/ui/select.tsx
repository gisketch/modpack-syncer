        "border-l-2 border-l-transparent",
        "hover:bg-surface-panel-strong hover:text-text-high",
        "data-highlighted:bg-surface-panel-strong data-highlighted:text-text-high",
        "data-[selected]:bg-brand-core/10 data-[selected]:border-l-brand-core data-[selected]:text-brand-core data-[selected]:font-bold",
        "data-disabled:pointer-events-none data-disabled:opacity-50",
        "[&_svg]:pointer-events-none [&_svg]:shrink-0 [&_svg:not([class*='size-'])]:size-4",
